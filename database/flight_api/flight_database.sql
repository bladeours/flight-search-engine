CREATE DATABASE IF NOT EXISTS flights;

USE flights;

CREATE TABLE `airport`(
                          `code` VARCHAR(3) NOT NULL PRIMARY KEY,
                          `city` VARCHAR(100) NOT NULL,
                          `country` VARCHAR(100) NOT NULL
);
CREATE TABLE `company`(
                          `id` INT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
                          `name` VARCHAR(255) NOT NULL
);

CREATE TABLE `flight` (
                          `id` int unsigned NOT NULL AUTO_INCREMENT PRIMARY KEY,
                          `departure_airport` varchar(3) NOT NULL,
                          `arrival_airport` varchar(3) NOT NULL,
                          `departure_date` datetime NOT NULL,
                          `flight_time` time NOT NULL,
                          `all_seats` int unsigned NOT NULL,
                          `free_seats` int unsigned NOT NULL,
                          `price` double NOT NULL,
                          `company_id` int unsigned NOT NULL,
                          `distance_km` int unsigned NOT NULL,
                          KEY `flight_departure_airport_arrival_airport_index` (`departure_airport`,`arrival_airport`),
                          KEY `flight_arrival_airport_foreign` (`arrival_airport`),
                          KEY `flight_company_foreign` (`company_id`),
                          CONSTRAINT `flight_arrival_airport_foreign` FOREIGN KEY (`arrival_airport`) REFERENCES `airport` (`code`),
                          CONSTRAINT `flight_company_foreign` FOREIGN KEY (`company_id`) REFERENCES `company` (`id`),
                          CONSTRAINT `flight_departure_airport_foreign` FOREIGN KEY (`departure_airport`) REFERENCES `airport` (`code`)
);

DELIMITER $$

CREATE TRIGGER after_flight_insert
    BEFORE INSERT
    ON flight FOR EACH ROW
BEGIN
    DECLARE percent float;
SET percent := NEW.free_seats / NEW.all_seats;
    IF percent < 0.25 THEN
        SET NEW.price = NEW.price * 0.9;
    ELSEIF percent < 0.5 THEN
        SET NEW.price = NEW.price * 1.1;
    ELSEIF percent < 0.75 THEN
       SET NEW.price = NEW.price * 1.2;
    ELSE
        SET NEW.price = NEW.price * 1.3;
END IF;
END$$

DELIMITER ;

DELIMITER $$

CREATE TRIGGER before_flight_update
    BEFORE UPDATE
    ON flight FOR EACH ROW
BEGIN
    DECLARE percent float;
    SET @percent := NEW.free_seats / OLD.all_seats;
    IF NOT (NEW.free_seats <=> OLD.free_seats) THEN
        IF percent > 0.25 AND percent < 0.5 THEN
            SET NEW.price = OLD.price * 1.1;
        ELSEIF percent < 0.75 THEN
           SET NEW.price = OLD.price * 1.2;
    ELSE
            SET NEW.price = OLD.price * 1.3;
END IF;
END IF;
END$$

DELIMITER ;

